class data_types {
    public static void main(String[] args) {
        int n = 24;
        float f = 34.4f;
        double d = 34.45;
        byte b = (byte)129; // 129 will be out of range so converted into byte by making cyclic turns
        char ch = 'A';

        System.out.println(n);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(ch);
    }
}
